const uint8_t* getCharData(char c) {
    if (isalpha(c)) {
        c = toupper(c);
    }

    for (size_t i = 0; i < sizeof(fontDataMap) / sizeof(fontDataMap[0]); ++i) {
        if (fontDataMap[i].character == c) {
            return fontDataMap[i].data;
        }
    }

    return NULL;
}

